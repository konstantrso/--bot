@bot.message_handler(commands=['start'])
def start(message):
    user_data[message.chat.id] = {'q_index': 0, 'answers': {}}
    bot.send_message(message.chat.id,
        "Добро пожаловать!\n\nЭто анкета Системный аудит долголетия.\n\nОтвечайте кратко. Если не знаете - напишите не знаю.\n\nДля начала нажмите /анкета")

@bot.message_handler(commands=['анкета'])
def start_survey(message):
    user_data[message.chat.id] = {'q_index': 0, 'answers': {}}
    ask_question(message.chat.id)

def ask_question(chat_id):
    data = user_data.get(chat_id)
    if not data:
        return
    idx = data['q_index']
    if idx < len(QUESTIONS):
        key, text = QUESTIONS[idx]
        bot.send_message(chat_id, text)
    else:
        finish_survey(chat_id)

@bot.message_handler(func=lambda m: True)
def handle_answer(message):
    chat_id = message.chat.id
    if chat_id not in user_data:
        bot.send_message(chat_id, "Напишите /start для начала")
        return
    data = user_data[chat_id]
    idx = data['q_index']
    if idx < len(QUESTIONS):
        key, _ = QUESTIONS[idx]
        data['answers'][key] = message.text
        data['q_index'] += 1
        ask_question(chat_id)

def finish_survey(chat_id):
    bot.send_message(chat_id, "Анкета завершена! Спасибо! Генерирую PDF...")
    try:
        data = user_data[chat_id]
        answers = data['answers']
        patient_name = answers.get('фио', 'Пациент')
        date_str = datetime.now().strftime("%d.%m.%Y %H:%M")
        pdf_path = f"/tmp/anketa_{chat_id}.pdf"
        generate_pdf(answers, QUESTIONS, pdf_path, patient_name, date_str)
        with open(pdf_path, 'rb') as f:
            bot.send_document(ADMIN_CHAT_ID, f,
                caption=f"Новая анкета\nПациент: {patient_name}\nДата: {date_str}")
        bot.send_message(chat_id, "Ваша анкета отправлена врачу! Ожидайте обратной связи.")
    except Exception as e:
        bot.send_message(chat_id, "Произошла ошибка. Свяжитесь с администратором.")
        print(f"Ошибка: {e}")

print("Бот запущен!")
